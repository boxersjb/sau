//Control Statement
//Loop 
//for, while, do-while

let x = 1
while (x <= 10 ){
    console.log(x, 'hello');
    x++
}
console.log("====================");

let y = 1
do {
    console.log(y, 'hi');
    y++
}while (y <= 10)
console.log("====================");

for (let i = 1; i <= 10; i++){
    console.log(i, 'Good');
}